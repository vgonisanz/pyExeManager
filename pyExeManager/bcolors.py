# -*- coding: utf-8 -*-
class bcolors:
    """ Helper class to define ANSI escape sequences to print with color """
    HEADER = '\033[95m'
    NORMAL = '\033[39m'
    OKBLUE = '\033[94m'
    OKGREEN = '\033[92m'
    WARNING = '\033[93m'
    FAIL = '\033[91m'
    ENDC = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'
